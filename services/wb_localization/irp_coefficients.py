# Порог ИРП-зоны: ниже этого — КРП > 0
IRP_THRESHOLD = 60.0


def get_ktr_krp(localization_pct: float) -> tuple[float, float]:
    """КТР и КРП по доле локализации артикула.

    Returns:
        (КТР, КРП%) — коэффициенты из таблицы WB.
    """
    loc = max(0.0, min(100.0, localization_pct))
    for min_loc, max_loc, ktr, krp in COEFF_TABLE:
        if min_loc <= loc <= max_loc:
            return ktr, krp
    return 2.20, 2.50


def get_zone(localization_pct: float) -> str:
    """Зона артикула по локализации.

    Returns:
        "ИРП-зона" | "ИЛ-зона" | "OK"
    """
    if localization_pct < IRP_THRESHOLD:
        return "ИРП-зона"
    elif localization_pct < 75.0:
        return "ИЛ-зона"
    return "OK"


def calc_financial_impact(
    krp_pct: float,
    price: float,
    orders: int,
    period_days: int,
) -> float:
    """Финансовый impact ИРП в рублях за месяц.

    Args:
        krp_pct: КРП в процентах (например, 2.10)
        price: розничная цена до скидки WB (₽)
        orders: количество заказов за period_days
        period_days: длина периода (дней)

    Returns:
        Убыток от ИРП в ₽/месяц.
    """
    if krp_pct <= 0 or price <= 0 or orders <= 0 or period_days <= 0:
        return 0.0
    daily_orders = orders / period_days
    monthly_orders = daily_orders * 30
    return krp_pct / 100.0 * price * monthly_orders


