def get_warehouse_fd(warehouse_name: str) -> str | None:
    """Маппинг склада WB → федеральный округ. None если неизвестный."""
    if warehouse_name in SKIP_WAREHOUSES:
        return None
    return WAREHOUSE_TO_FD.get(warehouse_name)


def get_delivery_fd(oblast: str) -> str | None:
    """Маппинг области доставки → федеральный округ.

    Сначала ищет в OBLAST_TO_FD, потом в FD_FULL_NAMES
    (на случай если API вернёт полное название ФО).
    """
    fd = OBLAST_TO_FD.get(oblast)
    if fd:
        return fd
    return FD_FULL_NAMES.get(oblast)


def log_unknown_mappings(orders: list[dict]) -> tuple[set[str], set[str]]:
    """Выводит warnings по незнакомым складам и областям.

    Returns:
        (unknown_warehouses, unknown_oblasts)
    """
    unknown_wh: set[str] = set()
    unknown_obl: set[str] = set()

    for o in orders:
        wh = o.get('warehouseName', '')
        if wh and wh not in WAREHOUSE_TO_FD and wh not in SKIP_WAREHOUSES:
            unknown_wh.add(wh)

        obl = o.get('oblastOkrugName', '') or o.get('oblast', '')
        if obl and not get_delivery_fd(obl):
            unknown_obl.add(obl)

    if unknown_wh:
        logger.warning(
            "Неизвестные склады WB (%d): %s",
            len(unknown_wh), sorted(unknown_wh)
        )
        print(f"   WARNING: Неизвестные склады ({len(unknown_wh)}): {sorted(unknown_wh)}")

    if unknown_obl:
        logger.warning(
            "Неизвестные области доставки (%d): %s",
            len(unknown_obl), sorted(unknown_obl)
        )
        print(f"   WARNING: Неизвестные области ({len(unknown_obl)}): {sorted(unknown_obl)}")

    return unknown_wh, unknown_obl
